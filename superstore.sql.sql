--QUERY 11 ADVANCED BUSINESS ANALYSIS:

Until now:

we analyzed totals
regions
categories
customers
monthly trends

Now we’ll analyze:

Discount Impact on Profit

This is a very realistic business problem.

Companies often ask:

“Are discounts reducing our profits?” 

SELECT 
     ROUND(AVG(DISCOUNT)*100,2)AS AVG_DISCOUNT_PERCENTAGE,
     ROUND(AVG(PROFIT),2) AS AVG_PROFIT
    FROM SUPERSALE;

Final SQL Analysis Step:

Profit Margin by Category 

This combines:

sales
profit
business efficiency

This is one of the most professional-looking analyses.

SELECT CATEGORY,
     SUM(SALES) AS TOTAL_SALES,
     SUM(PROFIT) AS TOTAL_PROFIT,
     ROUND((SUM(PROFIT)/SUM(SALES))*100,2)AS PROFIT_MARGIN_PERCENTAGE
FROM SUPERSALE
GROUP BY CATEGORY
ORDER BY PROFIT_MARGIN_PERCENTAGE DESC;

