--TOTAL SALES:

Business Meaning

Measures total company revenue generated from all orders.

Interview Explanation

“I used the SUM() aggregate function to calculate overall revenue generated by the business.”

SELECT SUM(SALES) AS TOTAL_SALES
FROM SUPERSALE;

--TOTAL PROFIT

Business Meaning

Shows actual earnings after costs.

Important Insight

Profit is much lower than sales

This may indicate:

high discounts
operational costs
low profit margins

SELECT SUM(PROFIT) AS TOTAL_PROFIT
FROM SUPERSALE;

--TOTAL ORDERS:

business meaning:

Represents total business transactions/orders processed.

SELECT COUNT(*) AS TOTAL_ORDERS
FROM SUPERSALE;


--QUERY 1: TOP SELLING PRODUCTS:

SELECT *
FROM(

SELECT PRODUCT_NAME,
        SUM(SALES)AS REVENUE
        FROM SUPERSALE
GROUP BY PRODUCT_NAME
ORDER BY REVENUE DESC
)
WHERE ROWNUM<=10;

--QUERY 2:REGION WISE SALES ANALYSIS:

SELECT REGION,SUM(SALES)AS TOTAL_SALES
FROM SUPERSALE
GROUP BY REGION
ORDER BY TOTAL_SALES DESC;

--QUERY 3:CATEGORY_WISE SALES

SELECT CATEGORY,SUM(SALES)AS TOTAL_SALES
FROM SUPERSALE
GROUP BY CATEGORY
ORDER BY TOTAL_SALES DESC;

--QUERY 4:MONTHLY SALES TREND ANALYSIS:

SELECT EXTRACT(MONTH FROM ORDER_DATE)AS MONTH,
SUM(SALES) AS MONTHLY_SALES
FROM SUPERSALE
GROUP BY EXTRACT(MONTH FROM ORDER_DATE)
ORDER BY MONTH;

--QUERY 5:TOP PROFITABLE PRODUCTS:

SELECT *
FROM(
     SELECT PRODUCT_NAME,
     SUM(PROFIT) AS TOTAL_PROFIT
     FROM SUPERSALE
     GROUP BY PRODUCT_NAME
     ORDER BY TOTAL_PROFIT DESC
)
WHERE ROWNUM <=10;

