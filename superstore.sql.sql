--QUERY 7 OVERALL PROFIT MARGIN ANALYSIS:

FORMULA PROFIT MARGIN=PROFIT/SALES *100

SELECT 
  ROUND((SUM(PROFIT)/SUM(SALES))*100,2)AS PROFIT_MARGIN_PERCENTAGE
FROM SUPERSALE;

