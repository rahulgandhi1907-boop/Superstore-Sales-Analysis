CREATE TABLE supersale (
    Row_ID NUMBER,
    Order_ID VARCHAR2(50),
    Order_Date DATE,
    Ship_Date DATE,
    Ship_Mode VARCHAR2(50),
    Customer_ID VARCHAR2(50),
    Customer_Name VARCHAR2(100),
    Segment VARCHAR2(50),
    Country VARCHAR2(50),
    City VARCHAR2(50),
    State VARCHAR2(50),
    Postal_Code NUMBER,
    Region VARCHAR2(50),
    Product_ID VARCHAR2(50),
    Category VARCHAR2(50),
    Sub_Category VARCHAR2(50),
    Product_Name VARCHAR2(255),
    Sales NUMBER(10,2),
    Quantity NUMBER,
    Discount NUMBER(5,2),
    Profit NUMBER(10,2)
);

select *from supersale;

select sales
